package java进阶.demo01Exercise;

public class Demo0102Student
{
    private String name;
    private int age;

    public Demo0102Student()
    {
    }

    public Demo0102Student(String name, int age)
    {
        this.name = name;
        this.age = age;
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public int getAge()
    {
        return age;
    }

    public void setAge(int age)
    {
        this.age = age;
    }
}
